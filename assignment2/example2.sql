--Create the transopse of Table
CREATE VIEW ttable
AS SELECT term, docid, count FROM atable
GROUP by term, docid;

CREATE VIEW stable
AS
SELECT
  atable.term, ttable.docid, SUM(atable.count * ttable.count)
FROM atable, ttable
WHERE atable.docid = ttable.docid
GROUP BY atable.docid, atable.term;


